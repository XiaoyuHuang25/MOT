__all__ = ["vis"]


def vis(img, boxes, scores, cls_ids, track_ids, conf=0.5, class_names=None, file_name=None):
    for i in range(len(boxes)):
        box = boxes[i]

        # Track ID
        track_id = int(track_ids[i]) if track_ids is not None else -1

        # Classification ID
        cls_id = int(cls_ids[i]) if cls_ids is not None else -1

        if scores is not None:
            score = scores[i]
            if score < conf:
                continue
        else:
            score = None

        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        # Color logic: use cls_id if available, otherwise default to 0
        color_id = cls_id if cls_ids is not None else 0
        color = (_COLORS[color_id] * 255).astype(np.uint8).tolist()

        # Text formatting
        if cls_ids is not None:
            class_name = class_names[cls_id] if class_names is not None else str(cls_id)
            if scores is not None:
                text = '{}-{}:{:.1f}%'.format(track_id, class_name, score * 100)
            else:
                text = 'Track{}-Class{}'.format(track_id, class_name)
        else:
            if scores is not None:
                text = 'Track{}:{:.1f}%'.format(track_id, score * 100)
            else:
                text = 'Track{}'.format(track_id)

        txt_color = (0, 0, 0) if np.mean(_COLORS[color_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[color_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5 * txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.6, txt_color, thickness=1)

    if file_name is not None:
        font = cv2.FONT_HERSHEY_SIMPLEX
        cv2.putText(img, file_name, (30, 60), font, 0.6, (0, 0, 0), thickness=1)

    return img


